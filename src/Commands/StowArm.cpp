#include "Commands/StowArm.h"
#include "Robot.h"

// ==========================================================================

StowArm::StowArm() {
	Requires(Robot::armSub);
}

// ==========================================================================

void StowArm::Initialize() {
	SetTimeout(1);
	Robot::armSub->StowArm();
}

// ==========================================================================

void StowArm::Execute() {
}

// ==========================================================================

bool StowArm::IsFinished() {
	return IsTimedOut();
}

// ==========================================================================

void StowArm::End() {
	Robot::armSub->DisablePositionControl();
}

// ==========================================================================

void StowArm::Interrupted() {
	End();
}

// ==========================================================================
