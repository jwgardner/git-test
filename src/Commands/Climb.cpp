#include "Commands/Climb.h"
#include "Robot.h"

// ==========================================================================

Climb::Climb() {
	Requires(Robot::winchSub);
}

// ==========================================================================

void Climb::Initialize() {
}

// ==========================================================================

void Climb::Execute() {
	Robot::winchSub->climb();
}

// ==========================================================================

bool Climb::IsFinished() {
	return false;
}

// ==========================================================================

void Climb::End() {
	Robot::winchSub->stopClimb();
}

// ==========================================================================

void Climb::Interrupted() {
	End();
}

