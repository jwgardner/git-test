#include "Commands/RunMotor.h"

// ==========================================================================

RunMotor::RunMotor(CANTalon *motor)
: _motor(motor) {
}

// ==========================================================================

void RunMotor::Initialize() {
}

// ==========================================================================

void RunMotor::Execute() {
	_motor->Set(1);
}

// ==========================================================================

bool RunMotor::IsFinished() {
	return false;
}

// ==========================================================================

void RunMotor::End() {
	_motor->Set(0);
}

// ==========================================================================

void RunMotor::Interrupted() {
	End();
}

// ==========================================================================
